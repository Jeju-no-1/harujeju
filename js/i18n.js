// Apply translations
function applyLanguage(lang) {
  const dict = translations[lang] || translations.ko;
  document.documentElement.lang = lang === 'ko' ? 'ko' : 'en';

  // Replace text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      // Preserve line breaks
      el.innerHTML = dict[key].replace(/\n/g, '<br>');
    }
  });

  // Replace attributes (placeholder, aria-label, etc)
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const config = el.dataset.i18nAttr.split(':');
    const attr = config[0];
    const key = config[1];
    if (dict[key]) el.setAttribute(attr, dict[key]);
  });

  // Update language switcher state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Save preference
  try { localStorage.setItem('haru_lang', lang); } catch (e) {}

  // Optional: add font class for english
  document.body.classList.toggle('lang-en', lang === 'en');
}

// Initial language detection
function getInitialLang() {
  try {
    const saved = localStorage.getItem('haru_lang');
    if (saved === 'ko' || saved === 'en') return saved;
  } catch (e) {}
  // Browser language fallback
  const browserLang = (navigator.language || 'ko').toLowerCase();
  return browserLang.startsWith('en') ? 'en' : 'ko';
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
  const initialLang = getInitialLang();
  applyLanguage(initialLang);

  // Wire up language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLanguage(btn.dataset.lang);
    });
  });
});

// Expose for debugging
window.applyLanguage = applyLanguage;
